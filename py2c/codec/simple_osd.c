int correct_orgxy(int *orgX, int *orgY, int width, int height)
{
    int ret = 0;
    int x = orgX[0];//[0,width)
    int y = orgY[0];//[0,height)
    int stride = FontWidth;
    if((x + stride) >= width)
    {
        x -=  stride;
        orgX[0] = x;
        ret = 1;
    }
    if((y + stride) >= height)
    {
        y -=  stride;
        orgY[0] = y;
        ret = 1;
    }
    return ret;
}
